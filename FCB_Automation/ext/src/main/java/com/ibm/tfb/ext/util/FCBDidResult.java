package com.ibm.tfb.ext.util;

public class FCBDidResult {
	public String workCode;
	public String rt;
	public FCBDidMsg msg;

	public String getWorkCode() {
		return workCode;
	}

	public void setWorkCode(String _workCode) {
		workCode = _workCode;
	}

	public String getRt() {
		return rt;
	}

	public void setRt(String _rt) {
		rt = _rt;
	}

	public FCBDidMsg getMsg() {
		return msg;
	}

	public void setMsg(FCBDidMsg _msg) {
		msg = _msg;
	}
}
